import Foundation
import UIKit

extension UIColor {
    class func navBarColor() -> UIColor {
        return UIColor(red: 15/255, green: 95/255, blue: 192/255, alpha: 1)
    }
    
    class func libraryHighlightColor() -> UIColor {
        return UIColor(red:194/255, green: 216/255, blue:255/255, alpha:1)
    }
}
